package TEMA4;

public class Condicionalif {
    
    public static void main(String[] args) {
        int numeroif = 8;

        if (numeroif < 0) {
            System.out.println("Numero Negativo");
        } else if (numeroif == 0) {
            System.out.println("Numero 0");
        } else {
            System.out.println("Numero Positivo");
        }
    }
}